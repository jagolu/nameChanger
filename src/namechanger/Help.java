    public void setHelp(int type){
        String message="";
        switch (type) {
            case 1: //Change sub dirs
                message+="This changes the dirs/files \ninside the dirs "
                        + "that are inside \nof the root dir you have chosen.";
                break;
            case 2: //Change files name
                message+="This changes the name of the \nfiles.";
                break;
            case 3:  //Begin with capital letters
                message+="This changes the first letter \nof the dir/file "
                        + "name to \ncapital letter.";
                break;
            case 4: //Erase accent marks
                message+="This changes the letters with \naccent marks to the "
                        + "same letter \nwithout the accent mark.";
                break;
            case 5: //Change dirs name
                message+="This changes the dirs name.";
                break;
            case 6: //Delete the last space
                message+="This changes the dir/file \nname from \"File .txt\" \nto "
                        + "\"File.txt\".";
                break;
            case 7: //Change the root dir name
                message+="This changes the root dir name.";
                break;
        }
        this.jTextArea1.setText(message);
    }
    
